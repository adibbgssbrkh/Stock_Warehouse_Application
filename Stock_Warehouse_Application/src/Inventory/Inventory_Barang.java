    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int i = tabel.getSelectedRow();
        
        if (i>-1){
            kode_barang.setText(model.getValueAt(i,0).toString());
            nama_barang.setText(model.getValueAt(i,1).toString());
            tanggal_masuk.setText(model.getValueAt(i,2).toString());
            tanggal_keluar.setText(model.getValueAt(i,3).toString());
            jumlah.setText(model.getValueAt(i,4).toString());
            kondisi.setText(model.getValueAt(i,5).toString());
            harga.setText(model.getValueAt(i,6).toString());
            
        }
    }//GEN-LAST:event_tabelMouseClicked

